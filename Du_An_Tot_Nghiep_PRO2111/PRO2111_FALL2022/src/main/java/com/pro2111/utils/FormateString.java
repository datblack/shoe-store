package com.pro2111.utils;

/**
 * @author BUI_QUANG_HIEU
 */
public class FormateString {

	/**
	 * In hóa tất cả chuỗi
	 * 
	 * @param value
	 * @return
	 */
	public static String UpperCaseString(String value) {
		return value.trim().toUpperCase();
	}

	/**
	 * In thường tất cả chuỗi
	 * 
	 * @param value
	 * @return
	 */
	public static String LowerCaseString(String value) {
		return value.trim().toLowerCase();
	}

}
